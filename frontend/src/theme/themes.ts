// Theme registry. Themes are data; applyTheme() writes the token contract to
// :root at runtime.
export type ThemeTokens = Record<string, string>

export interface Theme {
  id: string
  name: string
  pack: string
  dark: boolean
  tokens: ThemeTokens
}

interface Palette {
  bg: string
  surface: string
  surface2: string
  border?: string
  border2?: string
  text: string
  dim: string
  mut: string
  head?: string
  accent: string
  green: string
  orange: string
  red: string
  purple: string
  cyan: string
  star?: string
}

function rgba(hex: string, alpha: number): string {
  const normalized = hex.replace('#', '')
  const r = parseInt(normalized.slice(0, 2), 16)
  const g = parseInt(normalized.slice(2, 4), 16)
  const b = parseInt(normalized.slice(4, 6), 16)
  return `rgba(${r},${g},${b},${alpha})`
}

function createTheme(
  id: string,
  name: string,
  pack: string,
  dark: boolean,
  palette: Palette,
): Theme {
  return {
    id,
    name,
    pack,
    dark,
    tokens: {
      '--bg': palette.bg,
      '--surface': palette.surface,
      '--surface-2': palette.surface2,
      '--border': palette.border ?? palette.surface,
      '--border-2': palette.border2 ?? palette.surface2,
      '--text': palette.text,
      '--text-dim': palette.dim,
      '--text-mut': palette.mut,
      '--head': palette.head ?? palette.text,
      '--accent': palette.accent,
      '--accent-ink': palette.bg,
      '--accent-soft': rgba(palette.accent, dark ? 0.16 : 0.12),
      '--accent-line': rgba(palette.accent, dark ? 0.45 : 0.4),
      '--green': palette.green,
      '--orange': palette.orange,
      '--red': palette.red,
      '--purple': palette.purple,
      '--cyan': palette.cyan,
      '--star': palette.star ?? palette.orange,
      '--read-bg': palette.surface,
      '--read-text': palette.dim,
      '--grid': rgba(palette.accent, dark ? 0.06 : 0.05),
    },
  }
}

